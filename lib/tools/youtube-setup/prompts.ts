// 섹션별 프롬프트 — 병렬 호출용 (각각 짧고 빠름)
export function buildSectionPrompt(
  script: string,
  stage: ChannelStage,
  section: Section,
): string {
  const ctx = baseUserContext(script, stage);

  switch (section) {
    case "titles":
      return (
        ctx +
        `위 스크립트로 유튜브 제목을 정확히 10개 생성하세요.
- 제목 4대 원칙(물음표/트렌드/상위어/재분배 금지) 준수
- 채널 단계에 맞는 비중 조정
- 각각 다른 원칙 조합 활용 (다양성 확보)
- 60자 내외

오직 다음 JSON만 출력:
{
  "titles": [
    { "text": "...", "principles": ["물음표","상위어"], "reason": "한 줄" }
  ]
}`
      );

    case "thumbnails":
      return (
        ctx +
        `위 스크립트로 유튜브 썸네일 안을 정확히 10개 생성하세요.
- 자청 5대 원칙(표본/숫자/비교/구체성/위협) 다양하게 적용
- 글자 ≤15자, 모바일 가독성
- 제목과 시너지 (중복 X, 보완 O)

오직 다음 JSON만 출력:
{
  "thumbnails": [
    {
      "text": "≤15자 문구",
      "concept": "시각 컨셉 한 줄",
      "principles": ["숫자","비교"],
      "beforeAfter": { "before": "...", "after": "..." },
      "emotion": "놀람/분노/호기심/공포 등"
    }
  ]
}
beforeAfter 부적절한 주제는 null.`
      );

    case "description":
      return (
        ctx +
        `위 스크립트로 유튜브 설명란을 작성하세요.
- 첫 2~3줄(100~150자)에 핵심 키워드 Front-loading
- 본문 요약 3~5줄
- 챕터/타임스탬프 3개+ (00:00 형식)
- 해시태그 3개+
- 250자 이상

오직 다음 JSON만 출력:
{
  "description": "설명란 전체 본문 (줄바꿈 \\n 사용)"
}`
      );

    case "meta":
      return (
        ctx +
        `위 스크립트의 SEO 키워드와 체크리스트를 생성하세요.

오직 다음 JSON만 출력:
{
  "keywords": {
    "upper": ["상위어 5개"],
    "lower": ["하위어 10개"],
    "tags":  ["해시태그 10개 (# 없이 단어만)"]
  },
  "checklist": [
    { "item": "표본이론: 직관적 이해 가능?",       "pass": true,  "comment": "..." },
    { "item": "숫자: 구체적 숫자 포함?",            "pass": true,  "comment": "..." },
    { "item": "비교: Before/After 적용?",           "pass": false, "comment": "..." },
    { "item": "구체성: 장면이 선명히 그려지는가?",  "pass": true,  "comment": "..." },
    { "item": "위협: 손실 회피 + 내용 일치?",       "pass": true,  "comment": "..." },
    { "item": "YouTube 공식: 60자/Front-load/태그?", "pass": true, "comment": "..." }
  ]
}`
      );
  }
}

// 호환 fallback (사용 안 함)
export function buildUserPrompt(script: string, stage: ChannelStage): string {
  return baseUserContext(script, stage) + "JSON 하나만 출력.";
}
